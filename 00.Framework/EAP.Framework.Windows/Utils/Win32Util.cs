        /* Utility Method */

        public static void CenterScreen(Form form)
        {
            Screen activeScreen = Screen.FromHandle(form.Handle);
            int x = activeScreen.WorkingArea.Width / 2 - form.Width / 2;
            int y = activeScreen.WorkingArea.Height / 2 - form.Height / 2;

            form.Location = new Point(x, y);
        }
